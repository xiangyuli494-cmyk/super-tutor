"""Super Tutor — 自定义异常体系。

定义项目级异常树，让调用方能捕获细粒度错误类型，
避免依赖内置异常做控制流。
"""

# ===================================================================
# 基类
# ===================================================================


class TutorError(Exception):
    """Super Tutor 所有自定义异常的基类。

    项目内所有自定义异常都应继承自本类，
    使调用方能通过 ``except TutorError`` 捕获所有项目级错误。
    """


# ===================================================================
# 配置
# ===================================================================


class ConfigError(TutorError):
    """配置相关错误。

    在设置无效、必要文件缺失、配置值格式错误或路径安全违规时抛出。
    """


# ===================================================================
# LLM
# ===================================================================


class LLMError(TutorError):
    """LLM 调用层错误。

    在 API 调用重试耗尽、返回空内容或传输层故障时抛出。
    """


# ===================================================================
# 数据库
# ===================================================================


class DatabaseError(TutorError):
    """数据库操作错误。

    在初始化失败、查询异常或约束违反时抛出。
    """


# ===================================================================
# 教材
# ===================================================================


class MaterialError(TutorError):
    """教材相关错误的基类。"""


class MaterialNotFoundError(MaterialError):
    """教材未找到。"""


class MaterialProcessingError(MaterialError):
    """教材处理失败（如 PDF 解析异常）。"""

