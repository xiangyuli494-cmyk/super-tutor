"""自定义异常体系 — Super Tutor 项目级错误类型定义。

【功能说明】
定义三层异常树，使调用方能捕获细粒度错误类型，避免依赖内置异常做控制流。
所有自定义异常继承自 TutorError 基类。

【异常层级】
TutorError (基类)
├── LLMError — LLM API 调用层错误（超时、返回空内容、重试耗尽等）
└── MaterialError — 教材处理错误（PDF 解析失败、知识点提取失败等）

【耦合关系】
- 被 LLMClient 使用，抛出 LLMError
- 被 KnowledgeEngine、QuizEngine、SocraticEngine 使用，抛出 MaterialError
- 被 app.py 捕获并展示给用户
"""

# ===================================================================
# 基类 — 所有项目级异常的父类
# ===================================================================


class TutorError(Exception):
    """Super Tutor 所有自定义异常的基类。

    项目内所有自定义异常都应继承自本类，
    使调用方能通过 ``except TutorError`` 捕获所有项目级错误。
    """


# ===================================================================
# LLM 相关异常 — LLM API 调用失败
# ===================================================================


class LLMError(TutorError):
    """LLM 调用层错误。

    在以下情况抛出：
    - API 调用重试耗尽（超过 max_retries 次仍失败）
    - LLM 返回空内容（content is None）
    - API 超时（asyncio.TimeoutError）
    """


# ===================================================================
# 教材相关异常 — 教材解析/处理失败
# ===================================================================


class MaterialError(TutorError):
    """教材处理相关错误的基类。

    在以下情况抛出：
    - PDF 文件无法提取文本
    - LLM 返回的知识点 JSON 无法解析
    - LLM 返回的题目 JSON 无法解析
    - 提示词文件加载失败
    """
